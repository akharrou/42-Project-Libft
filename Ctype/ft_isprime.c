int		ft_isprime(int n)
{
	int i;

	if (n < 2)
		return (0);
	if (n == 2 || n == 3 || n == 5 || n == 7)
		return (1);
	if (n % 2 == 0 || n % 3 == 0 || n % 5 == 0 || n % 7 == 0)
		return (0);
	i = 7;
	while ((i * i) <= n)
	{
		if (n % i == 0)
			return (0);
		i += 2;
	}
	return (1);
}
